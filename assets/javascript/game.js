
    // Create Variables for the magic number, total score, wins, losses and each crystal
    $(document).ready(function() {
        var magicNumber;
        var totalScore = 0;
        var wins = 0;
        var losses = 0;
        var crystal1Num;
        var crystal2Num;
        var crystal3Num;
        var crystal4Num;

    // The player will be shown a random number at the start of the game. 
    // The computer will generate a random number between 19-120 
    
    
    function newNumbers() {
        magicNumber = Math.floor((Math.random() * 102) + 19);
		crystal1Num = Math.ceil(Math.random() * 12);
		crystal2Num = Math.ceil(Math.random() * 12);
		crystal3Num = Math.ceil(Math.random() * 12);
		crystal4Num = Math.ceil(Math.random() * 12);
    }
    
    // each image generate specific value generated by computer between 1-12

	function newGame() {
		newNumbers();
		totalScore = 0;
		$("#magicNumber").text(magicNumber);
		$("#totalScore").text(totalScore);
		$("#crystal1").attr("data-crystalvalue", crystal1Num);
		$("#crystal2").attr("data-crystalvalue", crystal2Num);
		$("#crystal3").attr("data-crystalvalue", crystal3Num);
		$("#crystal4").attr("data-crystalvalue", crystal4Num);
		$("#wins").text(wins);
		$("#losses").text(losses);
		$("#winOrLose").text("");

		
        }

        // Keep track of player's wins and losses
       

        function youWin() {
            $("#winOrLose").text("YOU WIN!");
            wins++;
            $("#wins").text(wins);
           
        }
    
        function youLose() {
            $("#winOrLose").text("YOU LOSE");
            losses++;
            $("#losses").text(losses);
            
        }
    
        newGame();
    
        $(".crystalimg").hover(function() {
            $(this).css({opacity: 0.4});
        },
        function() {
            $(this).css({opacity: 1});
        });
    
        // Function to add the crystal values together
        // if user score is = magic number, then user wins 
        // if user score is greater than magic number, then user loses

        $(".crystalimg").on("click", function() {
            if (totalScore >= magicNumber) {
                return;
            }
    
            var crystalValue = $(this).attr("data-crystalvalue");
            crystalValue = parseInt(crystalValue);
            totalScore += crystalValue;
            $("#totalScore").text(totalScore);
    
            if (totalScore === magicNumber) {
                youWin();
              
            } else if (totalScore > magicNumber) {
                youLose();
                
            }

        });
    // Function to reset the game

    });
            
        

        
